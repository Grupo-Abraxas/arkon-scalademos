CREATE EXTENSION Postgis;

CREATE TABLE public.activity (
	id serial4 NOT NULL,
	name varchar NOT NULL,
	CONSTRAINT activity_pkey PRIMARY KEY (id)
);

CREATE TABLE public.shop_category (
	id serial4 NOT NULL,
	name varchar NOT NULL,
	CONSTRAINT shop_category_pkey PRIMARY KEY (id)
);

CREATE TABLE public.stratum (
	id serial4 NOT NULL,
	name varchar NOT NULL,
	CONSTRAINT stratum_pkey PRIMARY KEY (id)
);

CREATE TABLE public.shop (
	id serial4 NOT NULL,
	"name" varchar NOT NULL,
	business_name varchar NOT NULL,
	activity_id int4 NOT NULL,
	stratum_id int4 NOT NULL,
	address varchar NOT NULL,
	phone_number varchar NOT NULL,
	email varchar NOT NULL,
	website varchar NOT NULL,
	shop_category_id int4 NOT NULL,
	"position" geography NOT NULL,
	CONSTRAINT shop_pkey PRIMARY KEY (id)
);

ALTER TABLE public.shop ADD CONSTRAINT activity FOREIGN KEY (activity_id) REFERENCES public.activity(id);
ALTER TABLE public.shop ADD CONSTRAINT shop_category FOREIGN KEY (shop_category_id) REFERENCES public.shop_category(id);
ALTER TABLE public.shop ADD CONSTRAINT stratum FOREIGN KEY (stratum_id) REFERENCES public.stratum(id);

